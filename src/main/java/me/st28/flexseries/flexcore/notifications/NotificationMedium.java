package me.st28.flexseries.flexcore.notifications;

/**
 * Represents a communication medium in which notifications can be sent to players with.
 */
public enum NotificationMedium {

    /**
     * Email notifications.
     */
    EMAIL,

    /**
     * The in-game notification system.
     */
    IN_GAME,

    /**
     * Text message notifications.
     */
    SMS

}